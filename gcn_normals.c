short find_gcn_normal(const float normal[3])
{
	int signx = normal[0] > 0.f ? 1 : 0;
	int signy = normal[1] > 0.f ? 1 : 0;
	int signz = normal[2] > 0.f ? 1 : 0;
	
	int start, end;
	
	int lowest = 0;
	float lowest_dist = FLT_MAX;
	
	if(!signx && !signy && !signz) {
		start = 0;
		end = 0x0129;
	} else if(!signx && !signy && signz) {
		start = 0x0129;
		end = 0x0261;
	} else if(!signx && signy && !signz) {
		start = 0x0261;
		end = 0x0399;
	} else if(!signx && signy && signz) {
		start = 0x0399;
		end = 0x04E1;
	} else if(signx && !signy && !signz) {
		start = 0x04E1;
		end = 0x0619;
	} else if(signx && !signy && signz) {
		start = 0x0619;
		end = 0x0761;
	} else if(signx && signy && !signz) {
		start = 0x0761;
		end = 0x08A9;
	} else {
		start = 0x08A9;
		end = 0x0A02;
	}
	
	for(; start != end; start++) {
		float dist = (normal[0] - gcn_normals[start].normal[0]) * (normal[0] - gcn_normals[start].normal[0]) +
		             (normal[1] - gcn_normals[start].normal[1]) * (normal[1] - gcn_normals[start].normal[1]) +
		             (normal[2] - gcn_normals[start].normal[2]) * (normal[2] - gcn_normals[start].normal[2]);
		             
		if(dist < lowest_dist) {
			lowest = start;
			lowest_dist = dist;
		}
	}
	
	return lowest;
}
